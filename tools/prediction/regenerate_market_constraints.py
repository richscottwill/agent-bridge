#!/usr/bin/env python3
"""Regenerate market-constraints.md from ps.market_constraints view.

Reads the single-source-of-truth view (which joins manual constraints with
market_status, forecast_tracker, targets, forecast_accuracy, regime_changes)
and emits market-constraints.md as auto-inclusion steering.

Writes to two locations:
- ~/.kiro/steering/market-constraints.md (user-level steering)
- ~/shared/.kiro/steering/market-constraints.md (shared steering)

Usage: python3 regenerate_market_constraints.py
Wired into: wbr-pipeline.sh step 8 (after forecast tracker update)
"""
import duckdb
import os
import sys
from datetime import datetime, timezone

sys.path.insert(0, os.path.expanduser('~/shared/tools'))
from prediction.config import MOTHERDUCK_TOKEN as TOKEN

STEERING_PATHS = [
    os.path.expanduser('~/.kiro/steering/market-constraints.md'),
    os.path.expanduser('~/shared/.kiro/steering/market-constraints.md'),
]

FRONT_MATTER = """---
inclusion: auto
---
# Market Constraints — Auto-Generated

Source of truth: `ps.market_constraints` view in MotherDuck (joins `ps.market_constraints_manual` with `ps.market_status`, `ps.forecast_tracker`, `ps.targets`, `ps.forecast_accuracy`, `ps.regime_changes`).

Regenerated by: `~/shared/tools/prediction/regenerate_market_constraints.py` (wired into WBR pipeline step 8). Manual fields are agent-editable via SQL UPDATE on `ps.market_constraints_manual`.

For structured queries from hooks / dashboards / scripts, query `ps.market_constraints` directly — do not parse this file.

"""

import math

def fmt_num(x, prec=0):
    if x is None or (isinstance(x, float) and math.isnan(x)):
        return "—"
    if prec == 0:
        return f"{int(round(x)):,}"
    return f"{x:,.{prec}f}"

def fmt_money(x):
    if x is None or (isinstance(x, float) and math.isnan(x)):
        return "—"
    return f"${int(round(x)):,}"

def fmt_pct(x, prec=1):
    if x is None or (isinstance(x, float) and math.isnan(x)):
        return "—"
    return f"{x:.{prec}f}%"

def _has(x):
    """None/NaN-safe truthiness check for pandas values."""
    if x is None:
        return False
    if isinstance(x, float) and math.isnan(x):
        return False
    return True

def render_market(row):
    """Render a single market section."""
    m = dict(row)
    lines = []
    lines.append(f"## {m['market']}")
    lines.append("")
    
    # Governing constraint + status
    lines.append(f"**Governing constraint:** {m['governing_constraint']}")
    lines.append(f"**Handoff status:** {m['handoff_status']}")
    lines.append(f"**OCI status:** {m['oci_status']}")
    lines.append(f"**CCP availability:** {m['ccp_availability']}")
    lines.append(f"**Next milestone:** {m['next_milestone']}")
    if _has(m.get('manual_notes')):
        lines.append(f"**Notes:** {m['manual_notes']}")
    lines.append("")
    
    # Latest actuals
    if _has(m.get('latest_week')):
        lines.append(f"**Latest week ({m['latest_week']}):** "
                    f"{fmt_num(m['last_week_regs'])} regs · "
                    f"{fmt_money(m['last_week_cost'])} · "
                    f"CPA {fmt_money(m['last_week_cpa'])}")
    
    # Projections
    if _has(m.get('next_week_predicted_regs')):
        regs_ci = ""
        if _has(m.get('next_week_ci_low_regs')) and _has(m.get('next_week_ci_high_regs')):
            regs_ci = f" (CI {fmt_num(m['next_week_ci_low_regs'])}–{fmt_num(m['next_week_ci_high_regs'])})"
        cost_str = ""
        if _has(m.get('next_week_predicted_cost')):
            cost_ci = ""
            if _has(m.get('next_week_ci_low_cost')) and _has(m.get('next_week_ci_high_cost')):
                cost_ci = f" (CI {fmt_money(m['next_week_ci_low_cost'])}–{fmt_money(m['next_week_ci_high_cost'])})"
            cost_str = f", {fmt_money(m['next_week_predicted_cost'])} cost{cost_ci}"
        lines.append(f"**Next-week projection:** {fmt_num(m['next_week_predicted_regs'])} regs{regs_ci}{cost_str}")
    
    # Monthly OP2
    if _has(m.get('month_op2_regs')):
        lines.append(f"**Current-month OP2:** {fmt_num(m['month_op2_regs'])} regs · "
                    f"{fmt_money(m['month_op2_cost'])} cost · "
                    f"CPA {fmt_money(m['month_op2_cpa'])}")
    
    # Forecast accuracy
    if _has(m.get('hit_rate_regs')):
        lines.append(f"**Forecast accuracy (regs):** {fmt_pct(m['hit_rate_regs'])} hit rate, "
                    f"{fmt_pct(m['avg_error_regs'])} avg error")
    
    lines.append("")
    
    # Structural baselines
    if _has(m.get('structural_baselines')):
        lines.append(f"**Structural baselines** ({m['structural_baseline_count']}):")
        for b in m['structural_baselines'].split(' ||| '):
            lines.append(f"- {b}")
        lines.append("")
    
    # Active impact regimes
    if _has(m.get('active_impact_regimes')):
        lines.append(f"**Active impact regimes** ({m['active_impact_count']}):")
        for r in m['active_impact_regimes'].split(' ||| '):
            lines.append(f"- {r}")
        lines.append("")
    
    # Recent past (ended <90 days ago, still relevant for narrative)
    if _has(m.get('recent_past_regimes')):
        lines.append(f"**Recently ended** ({m['recent_past_count']}):")
        for r in m['recent_past_regimes'].split(' ||| '):
            lines.append(f"- {r}")
        lines.append("")
    
    # Staleness indicator
    if _has(m.get('manual_updated_at')):
        age_days = (datetime.now(timezone.utc) - m['manual_updated_at'].replace(tzinfo=timezone.utc)).days
        stale_flag = " ⚠️ STALE" if age_days > 60 else ""
        lines.append(f"*Manual fields updated {m['manual_updated_at'].strftime('%Y-%m-%d')} "
                    f"by {m['manual_updated_by']} ({age_days}d ago){stale_flag}*")
    
    lines.append("")
    lines.append("---")
    lines.append("")
    return "\n".join(lines)


def render_footer():
    return """
## How to update

**Manual fields** (governing_constraint, handoff_status, OCI status, CCP availability, next_milestone, notes):
```sql
UPDATE ps.market_constraints_manual 
SET governing_constraint = '...', updated_at = CURRENT_TIMESTAMP, updated_by = '<agent-or-user>'
WHERE market = 'MX';
```
Agents may edit this table directly when a market shift is learned. The `updated_by` column is the audit trail.

**Auto-refreshing fields** (actuals, projections, targets, hit rate, regimes) — flow from the WBR pipeline weekly. No manual maintenance.

**Regime changes** — see `~/shared/wiki/agent-created/operations/regime-change-taxonomy.md` for when to insert a baseline vs impact regime, and how to set `half_life_weeks` + `precedent_source`.

## Structured query (preferred for scripts/hooks/dashboards)

```sql
SELECT * FROM ps.market_constraints WHERE market = 'MX';
```

Do NOT parse this markdown file in code. This file is the agent-context projection; `ps.market_constraints` is the source of truth.
"""


def main():
    print(f"Connecting to MotherDuck...")
    con = duckdb.connect(f'md:ps_analytics?motherduck_token={TOKEN}', read_only=True)
    
    print(f"Querying ps.market_constraints...")
    rows = con.execute("""
        SELECT * FROM ps.market_constraints 
        ORDER BY CASE market 
            WHEN 'WW' THEN 1 
            WHEN 'US' THEN 2 
            WHEN 'AU' THEN 3 
            WHEN 'MX' THEN 4 
            ELSE 5 
        END, market
    """).fetchdf()
    
    if len(rows) == 0:
        print("ERROR: ps.market_constraints returned 0 rows. Check the view.")
        sys.exit(1)
    
    print(f"Rendering {len(rows)} markets...")
    
    content_parts = [FRONT_MATTER]
    content_parts.append(f"**Last regenerated:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}")
    content_parts.append("")
    
    for _, row in rows.iterrows():
        content_parts.append(render_market(row))
    
    content_parts.append(render_footer())
    
    content = "\n".join(content_parts)
    
    for path in STEERING_PATHS:
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, 'w') as f:
            f.write(content)
        print(f"  ✓ Wrote {path} ({len(content)} bytes)")
    
    print(f"\n✓ Regenerated market-constraints.md for {len(rows)} markets.")
    con.close()


if __name__ == '__main__':
    main()
